function copyByRarity(rarity: DestinyCard["rarity"]): ResultCopy {
  if (rarity === "epic") {
    return {
      fortuneText: "高能星阵展开，这一包适合冲高光角色。",
      adviceText: "全员倒数三声，直接带着气势拆这一包。",
      buffText: "高能觉醒",
      barragePrompt: "高能觉醒！"
    };
  }
  if (rarity === "rare") {
    return {
      fortuneText: "粉金星轨亮起，高光角色感正在靠近。",
      adviceText: "开包前喊出解除封印，把期待点亮。",
      buffText: "星轨回应",
      barragePrompt: "星轨回应！"
    };
  }
  if (rarity === "hidden") {
    return cardCopyByName["无牌"];
  }
  return {
    fortuneText: "轻盈星辉靠近，适合稳稳拆这一包。",
    adviceText: "拆前停一秒，让星牌给直播间回应。",
    buffText: "星光回应",
    barragePrompt: "星光回应！"
  };
}

function getResultCopy(card: DestinyCard): ResultCopy {
  const text = `${card.name}${card.type}`;
  return cardCopyByName[card.name] ?? typeCopyByPattern.find(([pattern]) => pattern.test(text))?.[1] ?? copyByRarity(card.rarity);
}

function withResultCopy(card: DestinyCard): DestinyCard {
  return {
    ...card,
    ...getResultCopy(card)
  };
}

